package Circle;

public class Circle {

private double Pii;
private double radio;

public double getArea(){
    return Pii * radio * radio;
}
public double getPerimeter(){
    return Pii * radio;
}

public double getPii() {
    return Pii;
}
public void setPii(double pii) {
    this.Pii = pii;
}
public double getRadio() {
    return radio;
}
public void setRadio(double radio) {
    this.radio = radio;
}









}
